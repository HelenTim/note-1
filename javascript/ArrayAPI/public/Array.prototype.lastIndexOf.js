/*
*   Array.prototype.lastIndexOf 返回指定元素在数组中的最后一个索引 如果不存在则返回-1 从数组的后面往前查 从 fromIndex 开始
*   + 语法
*     arr.lastIndexOf(searchElement[, fromIndex])
*   + 参数
*     + searchElement
*     + fromIndex [可选] 开始查找的位置
*   + 返回值
*     数组中该元素最后一次出现的索引, 如未找到返回-1
*
*
* */

let arr = [1, 2, 3, 4, 5, 6];
console.log(arr.lastIndexOf(3)) // index 2

